export function findIanaAliases(ianaTimeZone: string) {
    const result = new Set();

    IANA_ALIAS_MAP.filter(function(_ref) {
        const alias = _ref.alias;

        return alias.includes(ianaTimeZone);
    }).map(function(it) {
        return it.alias;
    }).flat().forEach(function(it) {
        result.add(it);
    });

    return Array.from(result);
}

export function findIana(windowsTimeZone: string, territory: string) {
    const set = new Set();

    WINDOWS_TO_IANA_MAP.filter(function(it) {
        if (typeof territory === 'undefined') {
            return it.windowsName === windowsTimeZone;
        }

        return it.windowsName === windowsTimeZone && it.territory === territory;
    }).map(function(it) {
        return it.iana;
    }).flat().map(findIanaAliases).flat().forEach(function(alias) {
        set.add(alias);
    });

    return Array.from(set);
}

export function findWindows(ianaTimeZone: string) {
    const aliases = findIanaAliases(ianaTimeZone);
    const result = new Set();

    WINDOWS_TO_IANA_MAP.filter(function(it) {
        return it.iana.find(function(it) {
            return aliases.includes(it);
        });
    }).forEach(function(entry) {
        result.add(entry.windowsName);
    });

    return Array.from(result);
}

